$("#contactForm").on("submit", function(e) {
  e.preventDefault();
  $(".loading").removeClass("d-none");
  $(".error-message, .sent-message").addClass("d-none");

  $.ajax({
    url: "api/contact_process.php",
    type: "POST",
    data: $(this).serialize(),
    success: function(res) {
      $(".loading").addClass("d-none");
      if (res.status === "success") {
        $(".sent-message").removeClass("d-none");
        $("#contactForm")[0].reset();
      } else {
        $(".error-message").removeClass("d-none").text(res.message);
      }
    },
    error: function() {
      $(".loading").addClass("d-none");
      $(".error-message").removeClass("d-none").text("Something went wrong!");
    }
  });
});
</script>
